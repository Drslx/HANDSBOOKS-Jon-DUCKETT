/* Verifica se o valor e estritamente igual com if*/
let igualdade = 10;
if (igualdade === 10) {
  console.log("Igual a 10");
} else {
  console.log("Diferente de 10");
}

/* Verificar mas sem if */
let teste = 5 === 3 + 2;
console.log(teste);

/* Comparação estrita com uma string e um numero */
let teste2 = "5" === 3 + 2;
console.log(teste2);

/* Comparação nao estrita com uma string com um numero */
let teste3 = "5" == 3 + 2;
console.log(teste3);


